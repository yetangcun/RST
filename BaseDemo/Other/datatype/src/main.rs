fn main() {
    let integer = 2i32;
    println!("the integer val is {}", integer);

    let integer = 222u8;
    println!("the new integer val is {}", integer);

    let floatval:f32 = 16.8;
    println!("the f val is: {floatval}");

    let floatval = 16.88f64;
    println!("the f32 val is: {}", floatval);
}
